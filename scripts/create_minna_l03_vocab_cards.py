def wf(word: str) -> str:
    return f"<span class='study-word'>{word}</span>"


def make_card(
    *,
    theme: str,
    word: str,
    reading: str,
    translation: str,
    hint: str,
    jp: list[str],
    ru: list[str],
    notes: str,
    kanji: str,
    show_furi: str,
    extra_tags: list[str] | None = None,
) -> dict:
    jp1, jp2, jp3 = [x.replace("{{w}}", wf(word)) for x in jp]
    ex = f"{jp1}<br>{jp2}<br>{jp3}"
    ex_blank = ex.replace(wf(word), "_____")
    ex_ru = "<br>".join(ru)
    prompt = (
        "Иллюстрация учебная, 512×512, без японских надписей: "
        f"{ru[2]} Главный акцент — слово «{word}» и его роль в сцене."
    )
    tags = [theme, "みんなの日本語", "N5", MINNA_TAG]
    if extra_tags:
        tags.extend(extra_tags)
    return {
        "Слово": word,
        "Чтение": reading,
        "Перевод": translation,
        "Подсказка": hint,
        "Пример": ex,
        "Пример без слова": ex_blank,
        "Пример перевод": ex_ru,
        "Картинка": prompt,
        "Произношение": "",
        "Заметки": notes,
        "Кандзи-разбор": kanji,
        "Показать фуригану": show_furi,
        "Теги": tags,
    }


