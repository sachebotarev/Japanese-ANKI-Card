def merge_words(path: Path, add_words: list[str]) -> None:
    lines = [ln.strip() for ln in path.read_text(encoding="utf-8").splitlines() if ln.strip()]
    merged = sorted(set(lines) | set(add_words))
    path.write_text("\n".join(merged) + "\n", encoding="utf-8")


def main() -> None:
    words_by_theme: dict[str, list[str]] = {}
    for theme, data in CARDS:
        words_by_theme.setdefault(theme, []).append(data["Слово"])
        out = ROOT / "Карточки" / theme / f"{data['Слово']}.json"
        out.parent.mkdir(parents=True, exist_ok=True)
        out.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
        print(out.relative_to(ROOT))

    for theme, words in words_by_theme.items():
        slova = ROOT / "Слова" / theme
        if slova.exists():
            merge_words(slova, words)
            print(f"updated {slova.relative_to(ROOT)}")


if __name__ == "__main__":
    main()
