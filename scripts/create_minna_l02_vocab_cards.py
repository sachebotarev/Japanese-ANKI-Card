MINNA_TAG = "みんな初級I-第02課"


def wf(word: str) -> str:
    return f"<span class='study-word'>{word}</span>"


def blank(word: str, ex_full: str) -> str:
    return ex_full.replace(wf(word), "_____")


def card(
    theme: str,
    word: str,
    reading: str,
    trans: str,
    hint: str,
    jp1: str,
    jp2: str,
    jp3: str,
    ru1: str,
    ru2: str,
    ru3: str,
    notes: str,
    kanji: str,
    show_furi: str,
) -> dict:
    jp1 = jp1.replace("{{w}}", wf(word))
    jp2 = jp2.replace("{{w}}", wf(word))
    jp3 = jp3.replace("{{w}}", wf(word))
    ex = f"{jp1}<br>{jp2}<br>{jp3}"
    exb = blank(word, ex)
    exru = f"{ru1}<br>{ru2}<br>{ru3}"
    img_hint = (
        f"Иллюстрация учебная, 512×512, без японских подписей: {ru3} "
        f"Главный визуальный акцент — изучаемое слово/смысл карточки ({word})."
    )
    return {
        "Слово": word,
        "Чтение": reading,
        "Перевод": trans,
        "Подсказка": hint,
        "Пример": ex,
        "Пример без слова": exb,
        "Пример перевод": exru,
        "Картинка": img_hint,
        "Произношение": "",
        "Заметки": notes.replace("{{w}}", word),
        "Кандзи-разбор": kanji.replace("{{w}}", word),
        "Показать фуригану": show_furi,
        "Теги": [theme, "みんなの日本語", "名詞", "N5", MINNA_TAG],
    }


