def write_slova() -> None:
    pov = ROOT / "Слова" / "Повседневность"
    uch = ROOT / "Слова" / "Учёба"
    p_words = []
    u_words = []
    for theme, data in CARDS:
        w = data["Слово"]
        if theme == "Учёба":
            u_words.append(w)
        else:
            p_words.append(w)

    def merge(path: Path, add: list[str]) -> None:
        lines = [ln.strip() for ln in path.read_text(encoding="utf-8").splitlines() if ln.strip()]
        lines = sorted(set(lines) | set(add))
        path.write_text("\n".join(lines) + "\n", encoding="utf-8")

    merge(pov, p_words)
    merge(uch, u_words)


def main() -> None:
    write_slova()
    for theme, data in CARDS:
        w = data["Слово"]
        out = ROOT / "Карточки" / theme / f"{w}.json"
        out.parent.mkdir(parents=True, exist_ok=True)
        out.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
        print(out.relative_to(ROOT))


if __name__ == "__main__":
    main()
