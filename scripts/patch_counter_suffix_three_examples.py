def pronounce_field(word: str) -> str:
    return (
        f"[sound:{word}_слово.mp3][sound:{word}_пример_1.mp3]"
        f"[sound:{word}_пример_2.mp3][sound:{word}_пример_3.mp3]"
    )


def main() -> int:
    stems = sorted(p.stem for p in CARDS_DIR.glob("*.json"))
    missing_patch = sorted(s for s in stems if s not in DATA)

    missing_img: list[str] = []
    for stem in stems:
        exists = False
        for ext in (".png", ".jpg", ".jpeg", ".webp"):
            if (IMG_DIR / f"{stem}{ext}").is_file():
                exists = True
                break
        if not exists:
            missing_img.append(stem)

    if missing_patch:
        print("Ошибка: нет текста патча для:", ", ".join(missing_patch))
        return 1

    for word, (ex, tr, pic) in DATA.items():
        path = CARDS_DIR / f"{word}.json"
        data = json.loads(path.read_text(encoding="utf-8"))
        data["Пример"] = ex
        data["Пример без слова"] = blankify(ex)
        data["Пример перевод"] = tr
        data["Картинка"] = pic
        data["Произношение"] = pronounce_field(word)
        path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")

    lines = [
        "# Карточки без локальной картинки (следующий проход)",
        "",
        "Тема: **Счётные суффиксы**. Ожидается файл в `Картинки/Счётные суффиксы/<Слово>.(png|jpg|jpeg|webp)`.",
        "",
    ]
    if missing_img:
        lines.append("| Слово | JSON |")
        lines.append("| ----- | ---- |")
        for w in missing_img:
            lines.append(f"| `{w}` | `Карточки/{TOPIC}/{w}.json` |")
    else:
        lines.append("*(на момент обновления — все карточки темы имеют файл изображения; список пуст)*")
    lines.append("")
    REPORT.write_text("\n".join(lines), encoding="utf-8")
    print(f"Обновлено карточек: {len(DATA)}; отчёт без картинок: {REPORT.relative_to(ROOT)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
